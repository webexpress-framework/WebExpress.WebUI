namespace WebExpress.WebUI.WebIcon
{
    /// <summary>
    /// Defines the visual theme of an icon.
    /// </summary>
    public enum TypeIconTheme
    {
        /// <summary>
        /// The default theme renders icons using the bundled FontAwesome glyph font.
        /// </summary>
        Default,

        /// <summary>
        /// The light theme renders icons using the lightweight, line-style SVG variants
        /// shipped with WebExpress.WebUI.
        /// </summary>
        Light
    }
}
